<h2>Sobre</h2>
<div id = "div_sobre">

<p>A LAST, fundada no dia 20 de fevereiro de 2017 caracteriza-se por não ter fins
lucrativos, ter duração ilimitada, ser uma sociedade civil, não religiosa, apartidária e
vinculada à UninCor.</p><br>
<p>A LAST atuará no estudo, pesquisa e extensão, tendo como finalidade o
desenvolvimento, a promoção e a difusão de conhecimentos acerca da área sustentabilidade
e tecnologia, contribuindo para a formação acadêmica e profissional dos alunos a ela
vinculados.</p><br>
<p>Fica a cargo da LAST, através do desenvolvimento de projetos multidisciplinares e
multicêntricos, promover, facilitar e estimular a integração dos ligantes à tecnologia.</p><br>
<p>A LAST poderá firmar convênios e associações com entidade públicas e privadas
para atender suas finalidades e atribuições, assim como estabelecer parcerias.</p>
</div>
